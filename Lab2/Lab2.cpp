#include <stdio.h>
#include "converter.h"  // Подключаем заголовочный файл с объявлениями функций
#include <locale.h>

int main() {
    setlocale(LC_ALL, "Russian");
    int number;  // Переменная для хранения введенного числа

    printf("=== Конвертер десятичных чисел в двоичные ===\n");
    printf("Программа преобразует целые числа в двоичный код\n");
    printf("Для выхода из программы введите 0\n\n");

    // Основной цикл программы
    do {
        printf("Введите целое число: ");  // Приглашение для ввода
        scanf_s("%d", &number);  // Считываем число с клавиатуры

        if (number != 0) {  // Проверяем, не введен ли 0 для выхода
            printf("Десятичное число: %d\n", number);  // Выводим исходное число
            printf("Двоичное представление: ");  // Заголовок для двоичного вывода
            decimalToBinary(number);  // Вызываем функцию конвертации
            printf("\n\n");  // Переход на новую строку для красоты
        }
    } while (number != 0);  // Цикл продолжается пока не введен 0

    printf("Программа завершена. До свидания!\n");  // Сообщение о завершении
    return 0;  // Возвращаем 0 - успешное завершение программы
}