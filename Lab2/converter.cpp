    #include <stdio.h>   // Для функции printf
    #include "converter.h"  // Подключаем собственный заголовочный файл

    // Функция преобразования десятичного числа в двоичный код
    void decimalToBinary(int num) {
        unsigned int mask = 1 << 31;  // Создаем маску: 1000...000 (32 бита)
        int spaceCounter = 0;  // Счетчик для разделения тетрадами

        // Цикл по всем 32 битам числа
        for (int i = 0; i < 32; i++) {
            // Проверяем текущий бит с помощью поразрядной конъюнкции
            if (num & mask) {
                printf("1");  // Если бит установлен - выводим 1
            }
            else {
                printf("0");  // Если бит не установлен - выводим 0
            }

            spaceCounter++;  // Увеличиваем счетчик выведенных битов

            // Добавляем пробел после каждой тетрады (каждые 4 бита)
            if (spaceCounter == 4) {
                printf(" ");  // Выводим пробел-разделитель
                spaceCounter = 0;  // Сбрасываем счетчик
            }

            mask = mask >> 1;  // Сдвигаем маску вправо для проверки следующего бита
        }
    }